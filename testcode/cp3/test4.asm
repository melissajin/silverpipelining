
;
; Expected Values
; R0 = 0x1A
; R1 = 0x01
; R2 = 0x02
; R3 = 0x03
; R4 = 0x04
; R5 = 0x05
; R6 = 0x06
; R7 = 0x07
;

ORIGIN 0
SEGMENT
CODE:
	LEA R0, DATA
	ADD R1, R0, 0
	LDI R1, R1, LVAL1
	LDI R1, R1, 0
	LDI R1, R1, 0
	LDI R1, R1, 0
	STI R1, R0, LVAL1
	LDI R2, R0, LVAL1
DONE:
	ADD R3, R3, 1
	BR DONE
LVAL012:  DATA2 4xFFFF

SEGMENT
DATA:
LVAL1:  DATA2 LVAL2
LVAL2:  DATA2 LVAL3
LVAL3:  DATA2 SVAL1
SVAL1:  DATA2 SVAL2
SVAL2:  DATA2 SVAL3
SVAL3:  DATA2 SVAL4
SVAL4:  DATA2 GOOD
SVAL5:  DATA2 4x600D
GOOD:   DATA2 4x600D
BADD:   DATA2 4xBADD
